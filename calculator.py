def add(a,b):
    return a+b
def subtract(a,b):
    return a-b
def multiply(a,b):
    return a*b
def divide(a,b):
    return a/b
def modulus(a,b):
    return a%b
print("add:",add(26,7))
print("sub:",subtract(26,7))
print("multiply:",multiply(26,7))
print("divide:",divide(26,7))
print("modulus:",modulus(26,7))